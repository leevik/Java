import java.util.Scanner;

public class OperatorExpressions {
    public static void main(String[] args) {
        
    {
       Scanner sc = new Scanner(System.in);

       int a,b,c,x;
       double r1, r2;

       System.out.println("Enter values of a, b and c ");
       a=sc.nextInt();
       b=sc.nextInt();
       c=sc.nextInt();
       x=sc.nextInt();
       r1=sc.nextInt();
       r2=sc.nextInt();
       System.out.println(a+b+c+x+r1+r2);
       sc.close();
       
    }
    
}

}
